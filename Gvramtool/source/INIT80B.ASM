INIT80B:
	LD	HL, IOTBL
INIT80B_LOOP:
	LD	A, (HL)
	INC	HL
	LD	C, (HL)
	INC	HL
	OUT	(C), A
	INC	A
	JR	NZ, INIT80B_LOOP
	RET

IOTBL:
	DW	0E382h	; 8255設定 A=out B=in C=out
	DW	0E012h	; 8255 b4:ディスプレイ通常, b3-b0: CMT初期化
	DW	0E258h	; 8255 b3:BST=1 b4:OPEN=1 b6:WRITE=1
	DW	0E302h	; サウンド出力OFF
	DW	0E01Ah	; 8255 b4:ディスプレイ通常, b3:0,          b2-b0: CMT初期化
	DW	0E012h	; 8255 b4:ディスプレイ通常, b3:1(CMT停止), b2-b0: CMT初期化
	DW	0E30Dh	; 8255 b6:1 CMT録音解除、再生状態
	DW	0E734h	; 8253設定 カウンタ:0, カウンタリードライトモード:11(下位,上位の順に読み書き), カウンタモード: 010(モード2)
	DW	0E774h	; 8253設定 カウンタ:1, カウンタリードライトモード:11(下位,上位の順に読み書き), カウンタモード: 010(モード2)
	DW	0E7B4h	; 8253設定 カウンタ:2, カウンタリードライトモード:11(下位,上位の順に読み書き), カウンタモード: 010(モード2)
	DW	0E600h	; 8253 カウンタ2の下位: 00h
	DW	0E502h	; 8253 カウンタ1の下位: 02h
	DW	0E402h	; 8253 カウンタ0の下位: 02h
	DW	0E500h	; 8253 カウンタ1の上位: 00h: 0002h
	DW	0E400h	; 8253 カウンタ0の上位: 00h: 0002h
	DW	0E9CFh	; Z80-PIO Aポート設定 b7-b6:11(モード3), b3-b0:1111(モード設定)
	DW	0E900h	; Z80-PIO Aポート割り込みベクタ b7-b1:0000000 (Output)
	DW	0EBCFh	; Z80-PIO Bポート設定 b7-b6:11(モード3), b3-b0:1111(モード設定)
	DW	0EBFFh	; Z80-PIO Bポート割り込みベクタ b7-b1:1111111 (Input)
